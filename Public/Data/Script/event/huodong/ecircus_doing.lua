			EndEvent( sceneId )
			DispatchMissionDemandInfo( sceneId, selfId, targetId, x808005_g_ScriptId, -1, 2 )
		else
			x808005_MyNotifyTip( sceneId, selfId, "Bây gi¶ không phäi là th¶i gian hoÕt ğµng!" )
		end

	--¹ØÓÚ
	elseif key == x808005_g_Key["inf"] then
