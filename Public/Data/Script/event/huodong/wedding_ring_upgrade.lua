	local k = 0;
	--Ã»ÕÒµ½µÈc¤p¶ÔÓ¦toÕ ðµ ³¡¾°Ô´ÊýÄ¿
	if num == 0 then
		return -1, -1, -1;
	end
	-- ði¬mµ½µÚmµt cái³¡¾°
	local temp = mod( floor(random(100000)), num )+1;
	i = temp;
	-- ði¬mµ½µÚ¶þcái³¡¾°
	while 1 do
		temp =  mod( floor(random(100000)), num )+1;
		if temp ~= i then
			j = temp;
