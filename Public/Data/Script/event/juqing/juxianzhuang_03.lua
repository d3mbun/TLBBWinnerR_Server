		Msg2Player(  sceneId, selfId,"#YNhßn nhiÆm v¯#W: D˘ v’n ngﬂ∂i, ta vÁn c— ti™n",MSG2PLAYER_PARA )
		
		local misIndex = GetMissionIndexByID(sceneId,selfId,x200022_g_MissionId)
