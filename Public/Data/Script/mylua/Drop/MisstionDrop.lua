	if x045005_g_DropDetail[objdataId].Type == 1 then
		--Drop nhieu Ruong xung quanh Boss
		x045005_DropPublic(sceneId)
	else
		--Logic qq
		x045005_DropMe(sceneId, selfId, objdataId, objId)		
		if LuaFnHasTeam(sceneId, selfId) == 1 then
			local SceneTeamSize = LuaFnGetTeamSceneMemberCount(sceneId, selfId)
			if SceneTeamSize > 0 then
				--Thong Bao He Thong Khi Boss Chet
				local leaderId = GetTeamLeader( sceneId, selfId )
				if x045005_g_DropDetail[objdataId].Notice ~= "" then
					LuaFnAddGlobalCountNews(sceneId,leaderId)
					LuaFnAddGlobalCountNews(sceneId,GetName(sceneId,leaderId))
				end
				--Drop cho Thanh Vien trong team
				for i = 0, SceneTeamSize - 1 do
					playerId = LuaFnGetTeamSceneMember( sceneId, selfId, i )
					x045005_DropMe(sceneId, playerId, objdataId, objId)
				end
			end
		end
	end
end

function x045005_DropMe(sceneId, selfId, monsterId, monsterObj)
	if LuaFnIsObjValid(sceneId, selfId) ~= 1 then
		return
	end
	if LuaFnIsCanDoScriptLogic(sceneId, selfId) ~= 1 then
		return
	end
	
	local DropCount = 0;
	for i, Drop in x045005_g_DropDetail[monsterId].Dropbox do
		for j = 1, Drop.Num do
			local MonsterRand = random(1,x045005_g_DropDetail[monsterId].Const)
			if Drop.Const >= MonsterRand then
				local itemId = Drop.Item[random(1,getn(Drop.Item))]
				AddMonsterDropItem(sceneId, monsterObj, selfId, itemId)
				DropCount = DropCount + 1;
				if DropCount >= x045005_g_MaxDrop then
					break
				end
			end
		end
		if DropCount >= x045005_g_MaxDrop then
			break
		end
	end
end

