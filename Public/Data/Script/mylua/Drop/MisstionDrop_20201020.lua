function x045005_OnKillObject(sceneId, selfId, objdataId, objId)
	
	-- local PosX, PosY = GetWorldPos(sceneId, objId)
	-- LuaFnAddGlobalCountNews(sceneId,floor(PosX))
	-- LuaFnAddGlobalCountNews(sceneId,floor(PosY))
	
	--Kiem Tra Monster Co Nam Trong Danh Sach Drop
	if x045005_g_DropDetail[objdataId] == nil then
		return
	end

	--Drop Item Process
	--Logic qq
	if LuaFnHasTeam(sceneId, selfId) == 1 then
		local SceneTeamSize = LuaFnGetTeamSceneMemberCount(sceneId, selfId)
		if SceneTeamSize > 0 then
			--Drop cho Thanh Vien trong team
			for i = 0, SceneTeamSize - 1 do
				playerId = LuaFnGetTeamSceneMember( sceneId, selfId, i )
				x045005_DropMe(sceneId, playerId, objdataId, objId)
			end
		end
	end
	x045005_DropMe(sceneId, selfId, objdataId, objId)
end

function x045005_DropMe(sceneId, selfId, monsterId, monsterObj)
	if LuaFnIsObjValid(sceneId, selfId) ~= 1 then
		return
	end
	if LuaFnIsCanDoScriptLogic(sceneId, selfId) ~= 1 then
		return
	end
	
	--Monster Info
	SetMissionData(sceneId, selfId, DROP_MONSTER_TIME, LuaFnGetCurrentTime())
	SetMissionData(sceneId, selfId, DROP_MONSTER_INDEX, monsterId)
	
	--Reset Item Bind
	SetMissionData(sceneId, selfId, DROP_ITEM1_INDEX, 0)
	SetMissionData(sceneId, selfId, DROP_ITEM2_INDEX, 0)
	SetMissionData(sceneId, selfId, DROP_ITEM3_INDEX, 0)
	SetMissionData(sceneId, selfId, DROP_ITEM4_INDEX, 0)
	SetMissionData(sceneId, selfId, DROP_ITEM5_INDEX, 0)
	
	--Reset Item Notice
	SetMissionData(sceneId, selfId, DROP_ITEM1_NOTICE, 0)
	SetMissionData(sceneId, selfId, DROP_ITEM2_NOTICE, 0)
	SetMissionData(sceneId, selfId, DROP_ITEM3_NOTICE, 0)
	SetMissionData(sceneId, selfId, DROP_ITEM4_NOTICE, 0)
	SetMissionData(sceneId, selfId, DROP_ITEM5_NOTICE, 0)
	
	local DropCount = 0;
	for i, Drop in x045005_g_DropDetail[monsterId].Dropbox do
		local MonsterRand = random(1,x045005_g_DropDetail[monsterId].Const)
		local BoxConst = floor(Drop.Const)

		if BoxConst >= MonsterRand then
			--Add Item to Box
			local itemId = Drop.Item[random(1,getn(Drop.Item))]
			AddMonsterDropItem(sceneId, monsterObj, selfId, itemId)
			
			--Save ID Bind
			if Drop.Bind == 1 then
				SetMissionData(sceneId, selfId, DROP_ITEM1_INDEX+DropCount, itemId)
			end
			
			--Save ID Notice
			if Drop.Notice == 1 then
				SetMissionData(sceneId, selfId, DROP_ITEM1_NOTICE+DropCount, itemId)
			end
			
			--Count Item in Box
			DropCount = DropCount + 1;
			if DropCount >= x045005_g_MaxDrop then
				break
			end
		end

		if DropCount >= x045005_g_MaxDrop then
			break
		end
	end
end

