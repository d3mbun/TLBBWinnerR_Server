				x045060_Noitice(sceneId,selfId,"S¯ trang [nPage] không hþp l®!")
				return
			end
		
			if nPage == 1 then
				Run = 1;
				End = 20;
			elseif nPage == PageFull then
				Run = (nPage-1)*20 + 1;
				End = (nPage-1)*20 + ItemLast
			else
				Run = (nPage-1)*20 + 1;
				End = Run + 19;
			end
		end
	end

	for i = Run, End do
		StrItem_ID = StrItem_ID..x045060_g_ShopItem[nShop][i].ItemID;
		StrItem_Num = StrItem_Num..x045060_g_ShopItem[nShop][i].Num..",";
		local Item_Sale = x045060_g_ShopItem[nShop][i].Sale;
		local Item_Price = x045060_g_ShopItem[nShop][i].Price
		Item_Price = Item_Price*(100-Item_Sale)/100;
		StrItem_Price = StrItem_Price..Item_Price..",";
		StrItem_Time = StrItem_Time..x045060_g_ShopItem[nShop][i].Time..",";
	end
	
	local Sum = 1 + strlen(nPage..","..PageFull..",") + strlen(MoneyType) + strlen(StrItem_ID) + strlen(StrItem_Num) + strlen(StrItem_Price)
	if Sum >= 500 then
		x045060_Noitice(sceneId,selfId,"Shop chßa ð¸nh danh, vui lòng liên h® GM!")
		return
	end
	
	BeginUICommand( sceneId )
		UICommand_AddString( sceneId, "2" )
		UICommand_AddString( sceneId, nPage..","..PageFull.."," )
		UICommand_AddString( sceneId, MoneyType )
		UICommand_AddString( sceneId, StrItem_ID )
		UICommand_AddString( sceneId, StrItem_Num )
		UICommand_AddString( sceneId, StrItem_Price )
		UICommand_AddString( sceneId, StrItem_Time )
		EndUICommand( sceneId )
	DispatchUICommand( sceneId, selfId, 1021001 ) --7
end

