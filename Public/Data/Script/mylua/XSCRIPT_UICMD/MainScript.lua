			elseif EquipDW == 10155002 or (EquipDW >= 10172001 and EquipDW <= 10172170) then
				MaanxDW = 10172000 + TypeDW;
			elseif EquipDW == 10155003 or (EquipDW >= 10173001 and EquipDW <= 10173170) then
				MaanxDW = 10173000 + TypeDW;
			end
		end
		
		local MoneyJZ = GetMoneyJZ(sceneId,selfId)
		AddMoneyJZ(sceneId,selfId,MoneyJZ+10000000) --Add 5k JZ--
		while EquipDW ~= MaanxDW do
			x045001_EquipLevelUpEx( sceneId, selfId, ItemPos )
			EquipDW = LuaFnGetItemTableIndexByIndex(sceneId, selfId, ItemPos)
		end
		local CurMoneyJZ = GetMoneyJZ(sceneId,selfId)
		LuaFnCostMoneyWithPriority(sceneId,selfId,CurMoneyJZ-MoneyJZ)
	end
end

function x045001_GetDwAuthor(sceneId, selfId, ItemPos, type)
	local ItemID = LuaFnGetItemTableIndexByIndex(sceneId,selfId,ItemPos)
	local Author, szAuthor = LuaFnGetItemCreator(sceneId, selfId, ItemPos)
	if ItemID >= 10171001 and ItemID <= 10173170 then
		szAuthor = "*"..strsub(ItemID,6,8).."0000"
	elseif ItemID >= 10155001 and ItemID <= 10155003 then
		szAuthor = "*0000000"
	else
		if not szAuthor then
			szAuthor = "*0000000"
		end
	end

	local DWReturn = 0;
	if szAuthor then
		local Stick = strfind(szAuthor,"*")
		if Stick then
			if type == 1 then
				DWReturn = tonumber(strsub(szAuthor, Stick+1, Stick+3))
			elseif type == 11 then
				DWReturn = tonumber(strsub(szAuthor, Stick+4, Stick+7))
			end
		end
	end
	
	return DWReturn;
end

